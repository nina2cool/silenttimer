<p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Every year 
  hundreds of thousands of high school and college graduates aspire to higher 
  levels of education. We have all heard the buzzwords: <strong>SAT</strong>, 
  <strong>ACT</strong>, <strong>LSAT</strong>, <strong>MCAT</strong>, <strong>GRE</strong>, 
  <strong>GMAT</strong>, and the list goes on. These are all tests that help students 
  get into their college or graduate school. One feature all of these standardized 
  tests share is stringent time control to measure how well and how quickly students 
  can answer tough questions. In fact, the most difficult part of these exams 
  is the time constraint. Most students, if given ample time, could finish most 
  questions correctly.</font></p>
<p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Since accomplishing
     higher levels of education is paramount to a student&#8217;s future success,
