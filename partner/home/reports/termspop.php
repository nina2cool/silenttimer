<p><font color="#003399" size="2" face="Arial, Helvetica, sans-serif"><strong>Reporting Definitions</strong></font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><a name="1"></a><br>
  Total Clicks<br>
  </strong>This is the total number of times (this reporting period) people have 
  clicked through from your site to ours. The higher this number is, the more 
  people are coming to us from you.</font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><a name="2"></a><br>
  Total # of Sales</strong><br>
  This is the total number of products (this reporting period) your visitors
  have bought from us. If this number is 10, then you have sold 10 items though
  your account for this reporting period.</font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><a name="3"></a><br>
  Sale/Click Ratio</strong><br>
  This number is your <em>Total # of Sales divided by your Total Clicks</em>. 
  It lets you know how often a visitor from your site actually buys a product 
  when they click through to us.</font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif">If this ratio is low, that
    means that many students click through but are not purchasing. Try explaining
    time management and the timer more thoroughly on your site before sending
  your students to us.</font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><a name="4"></a><br>
  Total Sales</strong><br>
  This number is equal to the total amount of sales your site has generated.
  If you have sold 20 timers this reporting period, then this number might be
  ($29.95 * 20) $599.00.</font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><a name="5"></a> Commission
        on Sales </strong><br>
  This is the total number of sales times your commission rate. If your commission
  rate is $5 per sale, and you sold 20 timers, your commission would be $100.
  Your commission rate may also be a percentage of the order subtotal (timers,
books, batteries, etc) without shipping and tax. </font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><a name="8" id="8"></a>Total
        Returns </strong><br>
  The Total Returns reflects how many returns and/or cancelled orders occurred
  during the reporting period. The number is the total amount of the returns
  (not including shipping or tax). </font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><a name="9" id="9"></a>Commission
        on Returns </strong><br>
  This is the amount of commissions you would have earned on the return transactions,
  if they had not been returns or cancelled. Returned and cancelled orders are
  not valid for earning commission. </font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><a name="10" id="10"></a>Final
        Commission </strong><br>
  This is the amount of valid commissions you earned. The number is derived by
  subtracting the Commission on Returns from Commission on Sales. For example,
  if you earned $200 in commission, but had $10 in returns, your total earned
is $190. </font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><strong><a name="6" id="6"></a></strong><br>
  Closing Date</strong><br>
  Your closing date is the last date in a pay period. This is the day we cut off 
  sales for the current period to calculate your total commissions and send you 
  a check. It is usually the last day in each month.</font></p>
<p><font size="2" face="Arial, Helvetica, sans-serif"><strong><strong><a name="7" id="7"></a></strong><br>
  Date Sent</strong><br>
  This is the date we actually sent your check to you; it went in the mail on 
  this day. Remember that you only get a check if your account has $25 or more 
  in it.</font></p>
