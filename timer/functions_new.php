          9) When you finish the questions for
that passage, go back to Passage Count
Down by pressing # of P&rsquo;s again. You are now ready to read
                                                the next passage.<br>
                      <br>
          10) Now you will see that the
          number of passages has been reduced
          to 3, and that you still have
          4 minutes and 30 seconds per
          passage.<br>
          <br>
          11) When finished with the second passage, press # of Q&rsquo;s to
                                                go back to the Question Count Down
                  screen.<br>
          <br>
          12) This process is repeated until you
