          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">PASSAGE
                MODE is
                a new feature designed for test sections with passages or logic
                games. These sections include multiple reading passages (or logic
                games), each followed by a series of questions.
          The advantage of this feature is that it takes into account the amount
          of test time you will spend reading each passage and lets you know
          exactly how much time you can spend answering each question.</font></p>
          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">You
                do not have to use this feature. You can deactivate it when in Test
                    Setup Mode by pressing 3 until the &ldquo;PM&rdquo; disappears
                    from the screen. You cannot deactivate PASSAGE MODE while TEST MODE
                    is running.<br>
          <br>
          <strong><a name="p1"></a>Setting up PASSAGE MODE</strong><br>
          In order to use PASSAGE MODE, you need to have an idea of how much
    time you want or need to spend reading each passage. Sometimes it is
    recommended to spend three minutes or five minutes per passage (depending
    on your test).<br>
    <br>
          Choose the amount of time you want to spend reading each passage and
            then follow the steps below:<br>
            <em><br>
            Note: The time you input in this step will apply to every reading passage
          in the section.</em></font></p>
          <blockquote>
            <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">1) Press MODE until you get to the Passage Setup Mode screen.<br>
            2) Press TIME, and the leftmost digit will flash.<br>
            3) Enter your time in minutes, then seconds. For example, you want
            to spend 4 minutes and 30 seconds reading each passage, enter 0, 4,
            3, and then 0.<br>
            4) To enter the number of passages, press the button that says #
            of P&rsquo;s right above it. The right digits will flash.<br>
            5) Enter the number of passages. For four passages, enter 0 then 4.<br>
            6) Push # of P&rsquo;s again to make the screen stop flashing.</font></p>
          </blockquote>          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">          You are now set up to use PASSAGE MODE. In our example, you want
          to spend 4 minutes and 30 seconds reading each of the four passages.
          This takes a total of 18 minutes away from your total test time.</font></p>
          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">          <strong><a name="p2"></a>Using PASSAGE MODE for a Test:</strong><br>
          <br>
          1) After you have set up the PASSAGE MODE values (<a href="#p1">see
          steps above</a>),
      press MODE to get to Test Setup Mode.<br>
          <br>
          2) Press TIME and enter the amount of time you have to finish the
          entire section (all questions and passages). Continuing from our
          previous example, let&rsquo;s say you have 30 minutes for the four
                passage reading section.<br>
                <br>
          3) Press # of Q&rsquo;s and enter the number of questions
                          you have total (not for each passage). For our example,
                          we have 20 questions total on the test (5 questions
          per passage).</font></p>
          <blockquote>
            <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><em>Note:
                  The timer will say you have 1 minute and 30 seconds per
              question. This would be true if you did not have reading passages.</em></font></p>
          </blockquote>          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">4)
                Press # of Q&rsquo;s again to make the digits stop flashing.<br>
          <br>
          5) Now activate PASSAGE MODE by pressing 3.</font></p>
          <blockquote>
            <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">The
                    time per question changes to 36 seconds per question (instead of
                    1 minute, 30 seconds) because you have to spend 18 minutes of your
                    total test time of 30 minutes on reading the passages. You know
                    Passage Mode has been activated when &ldquo;PM&rdquo; appears
                to the left of the count down (or up) arrow.</font></p>
          </blockquote>          
          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><strong>Now
                you are set up to take the test. The next part describes how
                to tell the timer when you are finished with a passage and when
                you are finished with a question. Remember that a typical reading
                passage section has a reading section followed by questions,
                then another reading section, followed by more questions, and
                so on.</strong></font></p>
          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">6)
              Press Start/Stop and immediately press # of P&rsquo;s. Pressing
                                  # of P&rsquo;s takes you to the Passage Count
                                  Down screen, which allows you to see how much
                                  time you have remaining to finish reading the
          passage you are on.</font></p>
          <blockquote>
            <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><em>Note:
                  When you press Start/Stop, the timer starts counting down for the
                  first question. You are not ready for the first question
                                        because you have not read the passage first.
                                        That is why you switch to the Passage Count
              Down.</em></font></p>
          </blockquote>          
          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">7)
              Now read your passage. When you are finished reading the passage,
              you are ready to answer the questions. Quickly switch back to Question
            Count Down by pressing # of Q&rsquo;s. Pressing # of Q&rsquo;s
                            takes you to the Question Count Down screen, which
            allows you to see how much time you have remaining to answer the
          questions that follow the passage you just read.</font></p>
          <blockquote>
            <p align="left"><font size="2" face="Arial, Helvetica, sans-serif"><em>You
                    do NOT press the red button when finished with reading a passage.
