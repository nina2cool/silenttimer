              among the remaining questions on the test.</em></font></p>
          </blockquote>          <p align="left"><font size="2" face="Arial, Helvetica, sans-serif">Back
                  to our example, let&rsquo;s say you finished the reading passage
                                              one minute early. When you go back
                  to Question Count Down, you now have 39 seconds instead of 36
                  seconds per question. The remaining minute from the first passage
                  is distributed among the remaining questions (it does not add
                  more time to the remaining passages).<br>
          <br>
          8) Begin answering your questions normally, pressing
the red button as you finish each question.</font></p>
