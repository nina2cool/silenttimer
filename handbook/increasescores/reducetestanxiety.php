            self esteem and thoughts of failure, both which contribute to test
            anxiety. Practice positive &quot;self-talk&quot; by actually
              telling yourself that &quot;I can do this with enough practice,&quot; &quot;I
               am going to conquer this test with small steps at a time,&quot; &quot;I
