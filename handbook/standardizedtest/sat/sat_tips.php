<font size="2" face="Arial, Helvetica, sans-serif">If you have NO idea what the 
answer is, skip the question entirely. Points are deducted for wrong answers, 
not for skipped answers. If you can eliminate at least one answer choice, consider 
guessing (see process of elimination above). <font color="#FF0000">NOTE:</font> 
In the math section, do not omit any <em><strong>student-produced response (GRID) 
</strong></em> questions. When in doubt, guess on them because points are not 
deducted for wrong answers to these types of questions.<br>
