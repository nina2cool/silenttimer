      <p><font color="#003399" size="5" face="Arial, Helvetica, sans-serif"><strong>What 
        is CVV2?</strong></font></p>
      <p><strong><font color="#000000" size="3" face="Arial, Helvetica, sans-serif">Code 
        Verification Number 2</font></strong><br>
        <br>
        <font size="2" face="Arial, Helvetica, sans-serif">It is a new Internet 
        security feature that now appears on the back of most <a href="http://usa.visa.com/business/merchants/fraud_basics_cardnotpresent.html?it=search#link3b" target="_blank">Visa</a>, 
        <a href="http://usa.visa.com/business/merchants/fraud_basics_cardnotpresent.html?it=search#link3b" target="_blank"> 
        </a><a href="http://www.mastercard.com">MasterCard</a> &amp; <a href="http://www.discovercard.com/discover/data/account/securityprivacy/securityinfo.shtml" target="_blank">Discover 
        </a> cards, and on the front of <a href="http://www.americanexpress.com">American 
        Express cards</a>. This code helps validate that the customer placing 
        the online order actually has the credit card in his- or her possession, 
        and that the credit/debit card account is legitimate.</font></p>
      <p> <font size="2" face="Arial, Helvetica, sans-serif"><strong>How does 
        my Security Code protect me?</strong><br>
        The security code is only printed on the card and it is not contained 
        in the magnetic stripe information, nor does it appear on sales receipts 
        or billing statements. You must have the card in your possession to use 
        this code.</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif"><strong>Do I have 
        to enter this Security Code?</strong><br>
        If this code is printed on your card, YES. Visa, MasterCard, and American 
        Express now require Internet stores to obtain the security code for all 
        cards that have a code. In order for this transaction to be accepted and 
        processed, you must enter this code. This is for your protection.</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif"><strong>Where is the 
        Security Code?</strong></font></p>
